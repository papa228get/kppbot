/**
 * MenuFormatter - форматирование меню и справки
 */
class MenuFormatter {
  /**
   * Форматировать справку
   */
  static formatHelp(isAdmin) {
    let text = '📖 <b>Справка по использованию бота</b>\n\n';

    if (isAdmin) {
      text += '<b>Команды администратора:</b>\n';
      text += '/start - Главное меню\n';
      text += '/add - Добавить автомобиль\n';
      text += '/list - Показать все автомобили\n';
      text += '/stats - Показать статистику\n';
      text += '/import - Массовый импорт из файла\n';
      text += '/remove НОМЕР - Удалить автомобиль\n';
      text += '/cancel - Отменить текущую операцию\n';
      text += '/help - Эта справка\n\n';
      text += '<b>Проверка доступа:</b>\n';
      text += 'Просто отправьте номер автомобиля (например: А123БВ)\n\n';
      text += '<b>Массовый импорт:</b>\n';
      text += '1. Используйте команду /import\n';
      text += '2. Отправьте текстовый файл (.txt)\n';
      text += '3. Формат: Марка[TAB]Номер[TAB]Тип[TAB]Дата\n';
      text += '4. Пример: Лада Приора[TAB]В 782 РХ 12[TAB]Постоянный[TAB]31.12.2026\n';
    } else {
      text += '<b>Проверка доступа:</b>\n';
      text += 'Просто отправьте номер автомобиля (например: А123БВ)\n\n';
      text += 'Бот ответит:\n';
      text += '✅ ПРОПУСТИТЬ - если автомобиль разрешен\n';
      text += '⛔ НЕ ПРОПУСКАТЬ - если запрещен\n';
      text += '❌ Автомобиль не найден - если номера нет в базе\n';
    }

    return text;
  }
}

module.exports = MenuFormatter;
